abecedario = "bcdefghijklmnñopqrstuvwxyz"

if "a" in abecedario:
    print("a está en el abecedario")
else:
    print("a NO está en el abecedario")



if "z" not in abecedario:
    print("z no está en el abecedario")
else:
    print("z está en el abecedario")


