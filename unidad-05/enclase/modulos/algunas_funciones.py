



import math


print(" ceil() redondea el valor ingresado como argumento a su valor entero más cercano hacia arriba")
print(math.ceil(3.2))

print(" floor() redondea el valor ingresado como argumento a su valor entero más cercano hacia abajo")
print(math.floor(3.2))

print(" pow() devuelve el valor de x elevado a la potencia que le ingrese como argumento")
print(math.pow(2, 3))

print(" sqrt() devuelve la raíz cuadrada del valor ingresado como argumento")
print(math.sqrt(9))


