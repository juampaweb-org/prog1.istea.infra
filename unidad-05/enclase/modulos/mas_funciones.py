


# No hace falta llamar a el modulo math() porque ya estas funciones son nativas de Python

#abs() -> Devuelve el valor absoluto de un número, esto es, su valor sin signo.
print(abs(-23.23))

# round(num, n) -> Devuelve un número redondeado a n dígitos a la derecha del punto decimal.
# Si no se especifica n, se redondea al entero más cercano.
print(round(23.2345, 3))